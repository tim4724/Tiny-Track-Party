'use strict';
// Generate public/shared/trackSchematics.js — the REAL top-down schematic SVG for
// every catalogue track, precomputed from geometry. This lets dependency-free
// consumers (the no-relay controller gallery preview) render the actual track maps
// without pulling in Three.js: building a track needs TrackBuilder/Three, but the
// resulting SVG path is just data.
//
// In a live game the display computes these at runtime (trackSchematic) and ships
// them to phones in WELCOME — this file is only for surfaces that have no display.
//
// Run after editing any track layout:  node scripts/gen-track-schematics.js
// (tests/track.test.js fails if this file drifts from the live geometry.)
const fs = require('fs');
const path = require('path');

(async () => {
  const { buildTrack, TRACK_LIST } = await import('../public/display/TrackBuilder.js');
  const { trackSchematic } = await import('../public/display/trackSchematic.js');

  const body = TRACK_LIST
    .map((t) => `  ${t.id}: ${JSON.stringify(trackSchematic(buildTrack(t.segments)))}`)
    .join(',\n');

  const out =
    '// GENERATED by scripts/gen-track-schematics.js — DO NOT EDIT BY HAND.\n' +
    '// Real top-down track maps (precomputed from geometry) so dependency-free\n' +
    '// consumers can render actual schematics without Three.js. Regenerate after\n' +
    '// editing a track: node scripts/gen-track-schematics.js (track.test.js guards it).\n' +
    `export const TRACK_SCHEMATICS = {\n${body}\n};\n`;

  const dest = path.join(__dirname, '..', 'public', 'shared', 'trackSchematics.js');
  fs.writeFileSync(dest, out);
  console.log(`wrote ${path.relative(path.join(__dirname, '..'), dest)} (${TRACK_LIST.length} tracks)`);
})();
